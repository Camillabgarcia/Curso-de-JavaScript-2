//3 formas de declarar no JS : LET, CONST E VAR //
const estudante = 'Caroline';         //Se nunca for preciso mudar o valor da variável, utilizar o const, é necessário conter valor.
let professora;               //Quando se tem certeza que precisa mudar o valor da variável, pode não conter valor de primeira.

professora ='Ana';              //Valor atribuído após a criação do let.

console.log(estudante, professora);


//LET : é acessível fora do bloco condicional.
//VAR : não é acessível fora do bloco condicional.